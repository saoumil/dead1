
public class If {
	
	public static void main(String[] args) {
        int number = 20; // Declare and initialize the number variable

        // Check if the number is divisible by 2
        if (number % 2 == 0) {
            System.out.println(number + " is even."); // Print if the number is even
        } else {
            System.out.println(number + " is odd."); // Print if the number is odd
        }
    }
}
