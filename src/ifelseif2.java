
public class ifelseif2 {
	
	public static void main(String[] args) {
        int number = -3; // Input number

        // Check if the number is positive, negative, or zero
        if (number > 0) {
            System.out.println(number + " is positive."); // If the number is positive
        } else if (number < 0) {
            System.out.println(number + " is negative."); // If the number is negative
        } else {
            System.out.println("The number is zero."); // If the number is zero
        }
    }

}
