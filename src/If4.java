
public class If4 {
	
	public static void main(String[] args) {
        int age = 17; // Declare and initialize the age variable

        // Check if age is 18 or above
        if (age >= 18) {
            System.out.println("You are eligible to vote."); // Print if the person is eligible to vote
        } else {
            System.out.println("You are not eligible to vote."); // Print if the person is not eligible to vote
        }
    }

}
