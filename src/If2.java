
public class If2 {

	public static void main(String[] args) {
        int num1 = 15, num2 = 20; // Declare and initialize two numbers

        // Compare the two numbers
        if (num1 > num2) {
            System.out.println(num1 + " is larger."); // Print if num1 is larger
        } else {
            System.out.println(num2 + " is larger."); // Print if num2 is larger
        }
    }
}
