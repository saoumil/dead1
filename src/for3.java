
public class for3 {
	
	public static void main(String[] args) {
        int number = 5; // Number for which the table is printed

        // Loop to calculate and print the multiplication table
        for (int i = 1; i <= 10; i++) {
            System.out.println(number + " x " + i + " = " + (number * i));
        }
    }

}
