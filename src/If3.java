
public class If3 {
	
	public static void main(String[] args) {
        int number = 0; // Declare and initialize the number variable

        // Check if the number is positive, negative, or zero
        if (number > 0) {
            System.out.println(number + " is positive."); // Print if the number is positive
        } else if (number < 0) {
            System.out.println(number + " is negative."); // Print if the number is negative
        } else {
            System.out.println("The number is zero."); // Print if the number is zero
        }
    }

}
